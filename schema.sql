-- Fitness Club Database Schema
-- This database manages fitness clubs, members, trainers, workouts, and memberships

-- Drop triggers if they exist (for clean setup)
DROP TRIGGER IF EXISTS before_workout_schedule_insert;
DROP TRIGGER IF EXISTS after_attendance_insert;
DROP TRIGGER IF EXISTS after_attendance_delete;

-- Drop procedures if they exist (for clean setup)
DROP PROCEDURE IF EXISTS generate_loan_schedule;

-- Drop tables if they exist (for clean setup)
DROP TABLE IF EXISTS member_comments;
DROP TABLE IF EXISTS booking_cancellations;
DROP TABLE IF EXISTS loan_payment_schedule;
DROP TABLE IF EXISTS loans;
DROP TABLE IF EXISTS attendance;
DROP TABLE IF EXISTS workout_schedule;
DROP TABLE IF EXISTS memberships;
DROP TABLE IF EXISTS trainers;
DROP TABLE IF EXISTS members;
DROP TABLE IF EXISTS clubs;
DROP TABLE IF EXISTS workout_types;

-- Table: clubs
-- Stores information about fitness club locations
CREATE TABLE clubs (
    club_id INT PRIMARY KEY AUTO_INCREMENT,
    club_name VARCHAR(100) NOT NULL,
    address VARCHAR(255) NOT NULL,
    phone VARCHAR(20),
    email VARCHAR(100),
    opening_hours VARCHAR(100),
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

-- Table: members
-- Stores information about fitness club members/participants
CREATE TABLE members (
    member_id INT PRIMARY KEY AUTO_INCREMENT,
    first_name VARCHAR(50) NOT NULL,
    last_name VARCHAR(50) NOT NULL,
    email VARCHAR(100) UNIQUE NOT NULL,
    phone VARCHAR(20),
    date_of_birth DATE,
    gender ENUM('M', 'F', 'Other'),
    address VARCHAR(255),
    emergency_contact VARCHAR(100),
    emergency_phone VARCHAR(20),
    registration_date DATE NOT NULL,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

-- Table: trainers
-- Stores information about fitness trainers/coaches
CREATE TABLE trainers (
    trainer_id INT PRIMARY KEY AUTO_INCREMENT,
    first_name VARCHAR(50) NOT NULL,
    last_name VARCHAR(50) NOT NULL,
    email VARCHAR(100) UNIQUE NOT NULL,
    phone VARCHAR(20),
    specialization VARCHAR(100),
    certification VARCHAR(255),
    hire_date DATE NOT NULL,
    club_id INT,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    FOREIGN KEY (club_id) REFERENCES clubs(club_id) ON DELETE SET NULL
);

-- Table: memberships
-- Stores membership/subscription information for members
CREATE TABLE memberships (
    membership_id INT PRIMARY KEY AUTO_INCREMENT,
    member_id INT NOT NULL,
    club_id INT NOT NULL,
    membership_type ENUM('Basic', 'Premium', 'VIP') NOT NULL,
    start_date DATE NOT NULL,
    end_date DATE NOT NULL,
    price DECIMAL(10, 2) NOT NULL,
    status ENUM('Active', 'Expired', 'Suspended') DEFAULT 'Active',
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    FOREIGN KEY (member_id) REFERENCES members(member_id) ON DELETE CASCADE,
    FOREIGN KEY (club_id) REFERENCES clubs(club_id) ON DELETE CASCADE
);

-- Table: loans
-- Stores loan/installment plan information for membership payments
CREATE TABLE loans (
    loan_id INT PRIMARY KEY AUTO_INCREMENT,
    membership_id INT NOT NULL,
    principal DECIMAL(10, 2) NOT NULL,
    annual_interest_rate DECIMAL(5, 2) NOT NULL DEFAULT 0.00,
    term_months INT NOT NULL,
    start_date DATE NOT NULL,
    status ENUM('Active', 'Paid', 'Defaulted') DEFAULT 'Active',
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    FOREIGN KEY (membership_id) REFERENCES memberships(membership_id) ON DELETE CASCADE
);

-- Table: loan_payment_schedule
-- Stores monthly installment rows generated by generate_loan_schedule procedure
CREATE TABLE loan_payment_schedule (
    payment_id INT PRIMARY KEY AUTO_INCREMENT,
    loan_id INT NOT NULL,
    installment_number INT NOT NULL,
    due_date DATE NOT NULL,
    principal_amount DECIMAL(10, 2) NOT NULL,
    interest_amount DECIMAL(10, 2) NOT NULL,
    total_payment DECIMAL(10, 2) NOT NULL,
    remaining_balance DECIMAL(10, 2) NOT NULL,
    status ENUM('Pending', 'Paid', 'Overdue') DEFAULT 'Pending',
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    FOREIGN KEY (loan_id) REFERENCES loans(loan_id) ON DELETE CASCADE
);

-- Table: workout_types
-- Stores different types of workouts/classes offered
CREATE TABLE workout_types (
    workout_type_id INT PRIMARY KEY AUTO_INCREMENT,
    workout_name VARCHAR(100) NOT NULL,
    description TEXT,
    duration_minutes INT NOT NULL,
    difficulty_level ENUM('Beginner', 'Intermediate', 'Advanced') NOT NULL,
    max_participants INT DEFAULT 20,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

-- Table: workout_schedule
-- Stores scheduled workout sessions/classes
CREATE TABLE workout_schedule (
    schedule_id INT PRIMARY KEY AUTO_INCREMENT,
    workout_type_id INT NOT NULL,
    trainer_id INT NOT NULL,
    club_id INT NOT NULL,
    schedule_date DATE NOT NULL,
    start_time TIME NOT NULL,
    end_time TIME NOT NULL,
    available_spots INT,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    FOREIGN KEY (workout_type_id) REFERENCES workout_types(workout_type_id) ON DELETE CASCADE,
    FOREIGN KEY (trainer_id) REFERENCES trainers(trainer_id) ON DELETE CASCADE,
    FOREIGN KEY (club_id) REFERENCES clubs(club_id) ON DELETE CASCADE
);

-- Table: attendance
-- Tracks member attendance at scheduled workouts
CREATE TABLE attendance (
    attendance_id INT PRIMARY KEY AUTO_INCREMENT,
    schedule_id INT NOT NULL,
    member_id INT NOT NULL,
    attendance_date TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    status ENUM('Present', 'Absent', 'Cancelled') DEFAULT 'Present',
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    FOREIGN KEY (schedule_id) REFERENCES workout_schedule(schedule_id) ON DELETE CASCADE,
    FOREIGN KEY (member_id) REFERENCES members(member_id) ON DELETE CASCADE
);

-- Table: booking_cancellations
-- Tracks booking history when someone cancels their attendance
CREATE TABLE booking_cancellations (
    cancellation_id INT PRIMARY KEY AUTO_INCREMENT,
    schedule_id INT NOT NULL,
    member_id INT NOT NULL,
    original_booking_date TIMESTAMP NOT NULL,
    cancellation_date TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    FOREIGN KEY (schedule_id) REFERENCES workout_schedule(schedule_id) ON DELETE CASCADE,
    FOREIGN KEY (member_id) REFERENCES members(member_id) ON DELETE CASCADE
);

-- Table: member_comments
-- Stores employee comments about members (complaints, issues, etc.)
CREATE TABLE member_comments (
    comment_id INT PRIMARY KEY AUTO_INCREMENT,
    member_id INT NOT NULL,
    trainer_id INT NOT NULL,
    comment_text TEXT NOT NULL,
    comment_type ENUM('Complaint', 'Damage', 'Positive', 'Warning', 'Other') NOT NULL,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    FOREIGN KEY (member_id) REFERENCES members(member_id) ON DELETE CASCADE,
    FOREIGN KEY (trainer_id) REFERENCES trainers(trainer_id) ON DELETE CASCADE
);

-- Trigger: Auto-calculate end_time based on start_time and workout_type duration
DELIMITER //
CREATE TRIGGER before_workout_schedule_insert
BEFORE INSERT ON workout_schedule
FOR EACH ROW
BEGIN
    DECLARE duration INT DEFAULT 60;
    DECLARE max_spots INT DEFAULT 20;
    
    -- Get duration from workout_types (with default fallback)
    SELECT duration_minutes, max_participants INTO duration, max_spots
    FROM workout_types
    WHERE workout_type_id = NEW.workout_type_id;
    
    -- Calculate end_time by adding duration to start_time
    SET NEW.end_time = ADDTIME(NEW.start_time, SEC_TO_TIME(duration * 60));
    
    -- If available_spots is not provided, use max_participants from workout_types
    IF NEW.available_spots IS NULL THEN
        SET NEW.available_spots = max_spots;
    END IF;
END//
DELIMITER ;

-- Trigger: Decrease available_spots when a participant is added
DELIMITER //
CREATE TRIGGER after_attendance_insert
AFTER INSERT ON attendance
FOR EACH ROW
BEGIN
    UPDATE workout_schedule
    SET available_spots = available_spots - 1
    WHERE schedule_id = NEW.schedule_id;
END//
DELIMITER ;

-- Trigger: Increase available_spots and record cancellation when attendance is deleted
DELIMITER //
CREATE TRIGGER before_attendance_delete
BEFORE DELETE ON attendance
FOR EACH ROW
BEGIN
    -- Record the cancellation
    INSERT INTO booking_cancellations (schedule_id, member_id, original_booking_date)
    VALUES (OLD.schedule_id, OLD.member_id, OLD.attendance_date);
    
    -- Restore the available spot
    UPDATE workout_schedule
    SET available_spots = available_spots + 1
    WHERE schedule_id = OLD.schedule_id;
END//
DELIMITER ;

-- Procedure: generate_loan_schedule
-- Generates a monthly installment schedule for a given loan using the annuity formula:
--   M = P * r * (1+r)^n / ((1+r)^n - 1)
-- where P = principal, r = monthly interest rate, n = term in months.
-- Clears any existing schedule rows for the loan before regenerating.
-- When interest rate is 0, divides principal into equal monthly payments.
DELIMITER //
CREATE PROCEDURE generate_loan_schedule(IN p_loan_id INT)
BEGIN
    DECLARE v_principal DECIMAL(10, 2);
    DECLARE v_annual_rate DECIMAL(5, 2);
    DECLARE v_term_months INT;
    DECLARE v_start_date DATE;
    DECLARE v_monthly_rate DECIMAL(15, 10);
    DECLARE v_monthly_payment DECIMAL(10, 2);
    DECLARE v_balance DECIMAL(10, 2);
    DECLARE v_interest DECIMAL(10, 2);
    DECLARE v_principal_part DECIMAL(10, 2);
    DECLARE v_due_date DATE;
    DECLARE v_i INT DEFAULT 1;

    -- Fetch loan details
    SELECT principal, annual_interest_rate, term_months, start_date
    INTO v_principal, v_annual_rate, v_term_months, v_start_date
    FROM loans
    WHERE loan_id = p_loan_id;

    -- Remove existing schedule for this loan
    DELETE FROM loan_payment_schedule WHERE loan_id = p_loan_id;

    SET v_balance = v_principal;
    SET v_monthly_rate = v_annual_rate / 100.0 / 12.0;

    -- Calculate fixed monthly payment using annuity formula (or simple division if 0% interest)
    IF v_monthly_rate > 0 THEN
        SET v_monthly_payment = ROUND(
            v_principal * v_monthly_rate * POW(1 + v_monthly_rate, v_term_months)
            / (POW(1 + v_monthly_rate, v_term_months) - 1),
            2
        );
    ELSE
        SET v_monthly_payment = ROUND(v_principal / v_term_months, 2);
    END IF;

    WHILE v_i <= v_term_months DO
        SET v_due_date = DATE_ADD(v_start_date, INTERVAL v_i MONTH);
        SET v_interest = ROUND(v_balance * v_monthly_rate, 2);
        SET v_principal_part = v_monthly_payment - v_interest;

        -- Final installment: adjust to clear any rounding residual
        IF v_i = v_term_months THEN
            SET v_principal_part = v_balance;
            SET v_monthly_payment = v_principal_part + v_interest;
        END IF;

        SET v_balance = ROUND(v_balance - v_principal_part, 2);

        INSERT INTO loan_payment_schedule
            (loan_id, installment_number, due_date, principal_amount, interest_amount, total_payment, remaining_balance)
        VALUES
            (p_loan_id, v_i, v_due_date, v_principal_part, v_interest, v_monthly_payment, v_balance);

        SET v_i = v_i + 1;
    END WHILE;
END//
DELIMITER ;

